//compração de igualdade (≃=)
let spaceship="Golias"
let velocidade=80
console.log(velocidade==90)//falso
console.log(velocidade=="80")//verdadeiro
console.log(spaceship=="Golias")//verdadeiro

//comparação por tipo (===)
console.log(velocidade==="80")//falso

//comparação por diferença (!=)
console.log(velocidade!=80)//falso

//compração por diferença de tipo (!==)
console.log(velocidade!=="80")//true

//maior que  (>)
console.log(velocidade>70)
console.log(spaceship>"Helement")

//maior ou igual (>=)
console.log(velocidade>=70)
console.log(spaceship>="Golias")

//menor que  (<)
console.log(velocidade>70)
console.log(spaceship>"Helement")

//menor ou igual (<=)
console.log(velocidade<470)
console.log(spaceship<="Helement")